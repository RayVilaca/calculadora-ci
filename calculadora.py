def soma(a, b):
    """
    Essa função recebe dois números e retorna a soma deles.
    Parameters:
    a (int or float): O primeiro número a ser adicionado.
    b (int or float): O segundo número a ser adicionado.
    Returns:
    int or float: A soma dos dois números.
    """
    return a + b

def subtracao(a, b):
    """
    Essa função recebe dois números e retorna a subtração do primeiro pelo segundo.
    Parameters:
    a (int or float): O número do qual será subtraído.
    b (int or float): O número que será subtraído do primeiro.
    Returns:
    int or float: O resultado da subtração.
    """
    return a - b

def multiplicacao(a, b):
    """
    Essa função recebe dois números e retorna a multiplicação deles.
    Parameters:
    a (int or float): O primeiro número a ser multiplicado.
    b (int or float): O segundo número a ser multiplicado.
    Returns:
    int or float: O produto dos dois números.
    """
    return a * (b-1)